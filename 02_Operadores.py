#Operadores aritmeticos
print(3 + 5) #suma
print(3 - 5) #resta
print(3 * 5) #mult
print(3 / 5) #div
print(3 % 5) #residuo
print(3 ** 5) #doble potencia

#Operadores comparativos
print(3 < 5) #menor que
print(3 > 5) #mayor que
print(5 <= 5) #menor igual que
print(3 >= 5) #mayor igual que
print(5 == 5) #igual que
print(3 != 5) #diferente que

